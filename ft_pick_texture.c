#include "../incs/cub3d.h"

void		ft_pick_texture(t_ray *r)
{
	if (r->side == 1)
	{
		if (r->r_dir_y < 0)
			r->tex_code = 0;
		else
			r->tex_code = 1;
	}
	else
	{
		if (r->r_dir_x < 0)
			r->tex_code = 2;
		else
			r->tex_code = 3;
	}
}
